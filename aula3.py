""" (DocString)
 Python = Linguagem de programação 
 Tipo de tipagem = Dinâmica / Forte
 str -> string -> texto
 Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Alexandre Azevedo')

# Aspas duplas
print("Alexandre Azevedo")

# Escape
print("Alexandre \"Azevedo\"")

# r (serve para expressões regulares, geralmente)
print(r"Alexandre \"Azevedo\"")

# Outras combinações
print("'Alexandre Azevedo'")
print('"Alexandre Azevedo"')